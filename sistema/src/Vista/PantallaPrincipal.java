    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {
            PreparedStatement pps = cn.prepareStatement("INSERT INTO usuario (Nombre, Apellido, Edad, Correo) VALUE (?,?,?,?)");
            pps.setString(1, txtNombre.getText());
            pps.setString(2, txtApellido.getText());
            pps.setString(3, txtEdad.getText());
            pps.setString(4, txtCorreo.getText());
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos guardados en la base de datos.");
            limpiar();
            bloquear();
            mostrarTabla("");

        } catch (SQLException ex) {

            Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

