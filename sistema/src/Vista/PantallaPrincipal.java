    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        // eliminar datos de la tabla
        int fila = tblDatos.getSelectedRow();
        String valor = tblDatos.getValueAt(fila, 0).toString();

        if(fila >= 0){

            try {
                PreparedStatement ppt = cn.prepareStatement("DELETE FROM usuario WHERE Id = '"+valor+"'");
                ppt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Dato eliminado");
                limpiar();
                mostrarTabla("");

            } catch (SQLException ex) {

                Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        try {
            //actualizar datos
            PreparedStatement pps = cn.prepareStatement("UPDATE usuario SET Nombre ='"+txtNombre.getText()+"', Apellido ='"+txtApellido.getText()+"', Edad ='"+txtEdad.getText()+"', Correo ='"+txtCorreo.getText()+"' WHERE Id ='"+txtBuscar.getText()+"'");
            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos actualizados");
            limpiar();
            mostrarTabla("");

        } catch (SQLException ex) {

            Logger.getLogger(PantallaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        //modificar datos
        int fila = tblDatos.getSelectedRow();

        if(fila >= 0){

            txtBuscar.setText(tblDatos.getValueAt(fila, 0).toString());
            txtNombre.setText(tblDatos.getValueAt(fila, 1).toString());
            txtApellido.setText(tblDatos.getValueAt(fila, 2).toString());
            txtEdad.setText(tblDatos.getValueAt(fila, 3).toString());
            txtCorreo.setText(tblDatos.getValueAt(fila, 4).toString());

        }else{

            JOptionPane.showMessageDialog(null, "Fila no seleccionada");

        }
    }//GEN-LAST:event_btnModificarActionPerformed

