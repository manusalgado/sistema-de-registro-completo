    private void btnCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarActionPerformed

        File archivo;
        JFileChooser abrirArchivo = new JFileChooser();
        abrirArchivo.setFileFilter(new FileNameExtensionFilter("Extensiones de Imagenes", "jpg", "jpeg", "png"));

        int respuesta = abrirArchivo.showOpenDialog(this);

        if(respuesta == JFileChooser.APPROVE_OPTION){

            archivo = abrirArchivo.getSelectedFile();
            txtRuta.setText(archivo.getAbsolutePath());
            Image foto = getToolkit().getImage(txtRuta.getText());
            foto = foto.getScaledInstance(800, 500, 1);
            pnlImagen.setIcon(new ImageIcon(foto));
        }else{

            JOptionPane.showMessageDialog(null, "Error de formato");
        }
    }//GEN-LAST:event_btnCambiarActionPerformed

